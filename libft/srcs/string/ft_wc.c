#include "libft.h"

size_t			ft_wc(char const *s, char c)
{
	size_t	count;

	if (!(count = 0) && s)
		while (*s)
		{
			if (*s++ != c && ++count)
				while (*s != c && *s)
					s++;
			while (*s == c && *s)
				s++;
		}
	return (count);
}
