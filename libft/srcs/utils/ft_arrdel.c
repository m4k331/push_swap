#include "libft.h"

void	*ft_arrdel(void **mem)
{
	void	**pmem;

	pmem = *mem;
	if (mem && *mem)
	{
		while (*mem)
			ft_memdel(*mem++);
		ft_memdel(pmem);
	}
	return (pmem);
}
